int* mapOrder = new int[10];

void movement(unsigned char** map, int n, int m)
{
    int posX = 1;
    int posY = 1;
    map[posX][posY] = '*';
    char c = '0';
    while (1)
    {
        system("cls");

        for (int i = 0; i < n; i++)
        {
            for (int j = 0; j < m; j++)
            {
                cout << map[i][j];
            }
            cout << endl;
        }

        c = _getch();


        switch (c) {
        case 72:
        case 'W':
        case 'w':
            if (map[posX - 1][posY] != 'X' && map[posX - 1][posY] != 'F')
            {
                map[posX][posY] = ' ';
                map[posX - 1][posY] = '*';
                posX--;
                break;
            }
            else if (map[posX - 1][posY] == 'F')
            {
                goto level_complete;
            }
            else
            {
                break;
            }
        case 80:
        case 's':
        case 'S':
            if (map[posX + 1][posY] != 'X' && map[posX + 1][posY] != 'F')
            {
                map[posX][posY] = ' ';
                map[posX + 1][posY] = '*';
                posX++;
                break;
            }
            else if (map[posX + 1][posY] == 'F')
            {
                goto level_complete;
            }
            else
            {
                break;
            }
        case 75:
        case 'A':
        case 'a':
            if (map[posX][posY - 1] != 'X' && map[posX][posY - 1] != 'F')
            {
                map[posX][posY] = ' ';
                map[posX][posY - 1] = '*';
                posY--;
                break;
            }
            else if (map[posX][posY - 1] == 'F')
            {
                goto level_complete;
            }
            else
            {
                break;
            }
        case 77:
        case 'D':
        case 'd':
            if (map[posX][posY + 1] != 'X' && map[posX][posY + 1] != 'F')
            {
                map[posX][posY] = ' ';
                map[posX][posY + 1] = '*';
                posY++;
                break;
            }
            else if (map[posX][posY + 1] == 'F')
            {
                goto level_complete;
            }
            else
            {
                break;
            }
        }
    }
level_complete:
    system("cls");
    cout << "Level completed!";
    Sleep(2000);
}

void randomMapChooser()
{
    srand(time(0));
    for (int i = 0; i < 10; i++)
    {
        bool check;
        int n;
        do
        {

            n = (rand() % 10) + 1;


            check = true;
            for (int j = 0; j < i; j++)
                if (n == mapOrder[j])
                {
                    check = false;
                    break;
                }
        } while (!check);
        mapOrder[i] = n;
    }
}

